#! /usr/bin/env python

"""
Creates and returns a list of the ligands to filter.
"""


import Rules ## Module containing the Class eMolecule to build
             ## the object ligand to filter and the filtering
             ## rules methods

import pybel
import os



#################################
def ReadSDF(ext,file):
    """
    Creates and returns a list containing the objects generated by pybel.
    """
    file_molecules= pybel.readfile(ext,file)
    return file_molecules



#################################
def Create_Ligand(molecule):
    """
    Creates and returns a list containing the objects generated by eMolecule.
    These objects are create starting from the pybel objects built by ReadSDF(),
    and their own methods in addition to the methods of filtering from eMolecule.
    """
    
    ligand = Rules.eMolecule(sdf=molecule , id=molecule.title)
    return ligand 
